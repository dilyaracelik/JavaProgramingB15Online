package day07;

public class RelationalOperator {
    public static void main(String[] args) {

// > >= <<= == !=
//relational operator is used to compare 2 numbers and return true or false
  // any action or expression that return true or false result
  //can be called boolean expression

        System.out.println(7 > 5);
        System.out.println(7 < -19);
        System.out.println(5 >= 5 );
        System.out.println(7 >= 8);
        System.out.println(4 <= 5 );
        System.out.println( 17 >= 8 );

        System.out.println(5 == 5); //is used to check for equality  - like question are these equal?
        //and VERY different from single equal!!!
//Is difference between == and = same as difference
// between equal and equality in  daily English


        System.out.println( 6 != 10); //!= is used to check for inequality
        //  ! in programing translate to NOT
        // == and != are questions, checking for equality or inequality

        System.out.println( 6 != 6);  //saying 6 is not equal to 6 so FALSE

   //== means "true" IF EQUAL
   //!= means "true" IF NOT EQUAL

    }


}
