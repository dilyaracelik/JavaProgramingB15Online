package day07;

public class RelationalOperatorWork {
    public static void main(String[] args) {

        //EXAMPLE

        // > >= <<= == !=
//relational operator is used to compare 2 numbers and return true or false
        // any action or expression that return true or false result
        //can be called boolean expression

        int myNumber = 10;

//ALL of them are questions
        System.out.println(myNumber > 5);
        System.out.println(myNumber < -19);
        System.out.println(myNumber >= 5 );
        System.out.println(myNumber >= 8);
        System.out.println(myNumber <= 5 );
        System.out.println(myNumber >= 8 );

        System.out.println(myNumber == 5); //is used to check for equality  - like question are these equal?
        //and VERY different from single equal!!!
//Is difference between == and = same as difference
// between equal and equality in  daily English


        System.out.println( myNumber != 10); //!= is used to check for inequality
        //  ! in programing translate to NOT
        // == and != are questions, checking for equality or inequality

        System.out.println( myNumber != 6);  //saying 6 is not equal to 6 so FALSE

        //== means "true" IF EQUAL
        //!= means "true" IF NOT EQUAL






    }

}
